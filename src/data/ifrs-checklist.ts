// Group requirements by standard for easy access
export function getRequirementsByStandard(): Record<string, typeof ifrsRequirements> {
  const grouped: Record<string, typeof ifrsRequirements> = {};
  for (const req of ifrsRequirements) {
    if (!grouped[req.standard]) {
      grouped[req.standard] = [];
    }
    grouped[req.standard].push(req);
  }
  return grouped;
}

export function getStandardsList(): { id: string; name: string; count: number }[] {
  const grouped = getRequirementsByStandard();
  return Object.entries(grouped).map(([id, reqs]) => ({
    id,
    name: reqs[0].standardName,
    count: reqs.length,
  }));
}
